  		 LEFT OUTER JOIN ACT_CODE_MST B ON B.OFFICE_CD = A.OFFICE_CD AND B.ACCT_CD = A.ACCT_CD WHERE 1=1   AND A.OFFICE_CD = '@OFFICE_CD'UNIONSELECT A.COMN_CD AS ACCT_TYPE	 , '' AS ACCT_CD	 , '' AS ACCT_NM	 , '@OFFICE_CD' AS OFFICE_CD  FROM MDM_COM_CODE A WHERE 1=1   AND A.GRP_CD = 'S07'   AND A.COMN_CD NOT IN (SELECT A.ACCT_TYPE						   FROM MDM_OFFICE_ACCT A						  WHERE 1=1						    AND A.OFFICE_CD = '@OFFICE_CD'						 )  
